def missingNumbers(arr, brr):
    brr = sorted(brr)
    dict_brr = dict()
    for elem in brr:
        dict_brr[elem] = 0
    
    answer = []
    for key in dict_brr.keys():
        if arr.count(key) != brr.count(key):
            answer.append(key)
            
    return answer

