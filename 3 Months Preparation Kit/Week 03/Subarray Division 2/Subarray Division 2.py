def birthday(s, d, m):
    current = 0
    ways_count = 0
    while current < len(s):
        if len(s) - current < m:
            return ways_count
        if sum(s[current:current + m]) == d:
            ways_count += 1
            current += 1
        else:
            current += 1
    return ways_count

