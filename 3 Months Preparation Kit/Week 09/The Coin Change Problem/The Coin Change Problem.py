def getWays(n, c):
    # Write your code here
    sums = []
    for i in range(n + 1):
        sums.append(0)
    sums[0] = 1
    
    for coin in sorted(c):
        for i in range(len(sums)):
            if coin <= i:
                sums[i] += sums[i - coin]
                
    return sums[-1]
        
            
    
    

